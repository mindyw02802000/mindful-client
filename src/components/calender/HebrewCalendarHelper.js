  // פונקציה לקבלת תאריך עברי מתאריך לועזי
  getHebrewDate: function(date) {
    const dateString = date.toISOString().split('T')[0];
    return this.hebrewDatesMap[dateString] || { day: "", month: "", year: "" };
  },
  
  // בדיקה האם התאריך הוא שבת
  isShabbat: function(date) {
    return date.getDay() === 6; // 6 = שבת
  },
  
  // בדיקה האם התאריך הוא ערב שבת
  isErevShabbat: function(date) {
    return date.getDay() === 5; // 5 = יום שישי
  },
  
  // פונקציה לקבלת שם החודש העברי
  getHebrewMonthName: function(date) {
    const hebrewDate = this.getHebrewDate(date);
    return hebrewDate.month || "";
  },
  
  // פונקציה לקבלת היום בחודש העברי
  getHebrewDay: function(date) {
    const hebrewDate = this.getHebrewDate(date);
    return hebrewDate.day ? this.hebrewDays[hebrewDate.day - 1] : "";
  },
  
  // פונקציה לקבלת השנה העברית
  getHebrewYear: function(date) {
    const hebrewDate = this.getHebrewDate(date);
    return hebrewDate.year || "";
  },
  
  // פונקציה לקבלת התאריך העברי המלא כמחרוזת
  getHebrewDateString: function(date) {
    const hebrewDate = this.getHebrewDate(date);
    if (!hebrewDate.day) return "";
    
    return `${this.hebrewDays[hebrewDate.day - 1]} ${hebrewDate.month} ${hebrewDate.year}`;
  }
